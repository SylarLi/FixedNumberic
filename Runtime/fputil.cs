        public static fp128 Multi64U(ulong left, ulong right)
        {
            var lo = Unity.Burst.Intrinsics.Common.umul128(left, right, out var hi);
            return new fp128(hi, lo);
        }

        [MethodImpl(MethodImplOptions.AggressiveInlining)]
        public static fp128 Multi128U(fp128 left, fp128 right)
        {
            var fp = Multi64U(left.m_lo, right.m_lo);
            fp = new fp128(left.m_hi * right.m_hi, fp.m_hi);
            fp += Multi64U(left.m_hi, right.m_lo);
            fp += Multi64U(left.m_lo, right.m_hi);
            return fp;
        }

        [MethodImpl(MethodImplOptions.AggressiveInlining)]
        internal static fp192 Multi192U(fp192 left, fp192 right)
        {
            var ret = fp192.Zero;
            fp192 fp;
            if (left.m_oo != 0)
            {
                fp = Multi64U(left.m_oo, right.m_lo);
                ret += new fp192(0, 0, fp.m_hi);
                fp = Multi64U(left.m_oo, right.m_hi);
                ret += new fp192(0, fp.m_hi, fp.m_lo);
            }

            if (right.m_oo != 0)
            {
                fp = Multi64U(left.m_lo, right.m_oo);
                ret += new fp192(0, 0, fp.m_hi);
                fp = Multi64U(left.m_hi, right.m_oo);
                ret += new fp192(0, fp.m_hi, fp.m_lo);
            }

            fp = Multi64U(left.m_lo, right.m_lo);
            ret += new fp192(left.m_hi * right.m_hi, fp.m_hi, fp.m_lo);
            ret += Multi64U(left.m_hi, right.m_lo);
            ret += Multi64U(left.m_lo, right.m_hi);
            return ret;
        }

        [MethodImpl(MethodImplOptions.AggressiveInlining)]
        [return: Unity.Burst.CompilerServices.AssumeRange(0, 192)]
        internal static int Clz(fp192 x)
        {
            return x.m_hi != 0
                ? Clz(x.m_hi)
                : (x.m_lo != 0 ? Clz(x.m_lo) : Clz(x.m_oo) + 64) + 64;
        }

