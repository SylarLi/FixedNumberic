        [BurstDiscard]
        [MethodImpl(MethodImplOptions.AggressiveInlining)]
        public static int Clz(BigInteger x)
        {
            var ret = 0;
            var bytes = x.ToByteArray(false, true);
            var uc = bytes.Length / 8;
            for (var i = 0; i < uc; i++)
            {
                var b8 = BitConverter.ToUInt64(new ReadOnlySpan<byte>(bytes, i * 8, 8));
                var cz = Unity.Mathematics.math.tzcnt(b8);
                ret += cz;
                if (cz >= 64) continue;
                return ret;
            }

            for (var i = uc * 8; i < bytes.Length; i++)
            {
                var cz = Unity.Mathematics.math.min(Unity.Mathematics.math.tzcnt((uint) bytes[i]), 8);
                ret += cz;
                if (cz < 8) break;
            }

            return ret;
        }

